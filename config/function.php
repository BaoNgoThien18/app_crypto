function hideNumber($num) {
    $r = '';

    for ($i=0; $i<strlen($num); $i++) {
      if ($i < strlen($num) - 4) $r .= '*';
      else $r .= $num[$i];   
    }

    return $r;
}

function auto_decimal_format($n, $def = 2) {
    $a = explode(".", $n);
    if (count($a)>1) {
        $b = str_split($a[1]);
        $pos = 1;
        foreach ($b as $value) {
            if ($value != 0 && $pos >= $def) {
                $c = number_format($n, $pos);
                $c_len = strlen(substr(strrchr($c, "."), 1));
                if ($c_len > $def) { return rtrim($c, 0); }
                return $c; // or break
            }
            $pos++;
        }
    }
    return number_format($n, $def);
}
